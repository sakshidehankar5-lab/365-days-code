# Assign values
a = 10
b = 3

# Addition
print("Addition:", a + b)

# Subtraction
print("Subtraction:", a - b)

# Multiplication
print("Multiplication:", a * b)

# Division (float result)
print("Division:", a / b)

# Floor Division (integer result)
print("Floor Division:", a // b)

# Modulus (remainder)
print("Modulus:", a % b)

# Exponentiation (power)
print("Exponentiation:", a ** b)